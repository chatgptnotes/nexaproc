/**
 * Creates a deep copy of the tags array with values randomly walked
 * from their current positions (small deltas within range).
 * Digital tags randomly toggle with a 5% probability.
 */
export function generateRealtimeValues(currentTags?: ProcessTag[]): ProcessTag[] {
  const source = currentTags ?? tags;
  const now = new Date().toISOString();

  return source.map((tag) => {
    const updated = { ...tag, timestamp: now };

    if (tag.type === 'digital_input' || tag.type === 'digital_output') {
      // 5% chance of toggling
      if (Math.random() < 0.05) {
        updated.currentValue = tag.currentValue === 0 ? 1 : 0;
      }
      return updated;
    }

    // Analog: random walk +-1% of range
    const range = tag.maxRange - tag.minRange;
    const maxDelta = range * 0.01;
    const delta = (Math.random() - 0.5) * 2 * maxDelta;
    let newVal = tag.currentValue + delta;
    // Clamp to range
    newVal = Math.max(tag.minRange, Math.min(tag.maxRange, newVal));
    updated.currentValue = parseFloat(newVal.toFixed(3));

    // Occasionally degrade quality (1% chance)
    if (Math.random() < 0.01) {
      updated.quality = Math.random() < 0.5 ? 'uncertain' : 'bad';
    } else if (tag.quality !== 'good' && Math.random() < 0.1) {
      // 10% chance of recovering to good
      updated.quality = 'good';
    }

    return updated;
  });
}

